package main

import "fmt"

func RelationalOperators() {
	// Example of relational operators
	fmt.Println("List of Relational Operators in GoLang")		
	fmt.Println("Equal to: 10 == 5 =", 10 == 5)
	fmt.Println("Not equal to: 10 != 5 =", 10 != 5)
	fmt.Println("Greater than: 10 > 5 =", 10 > 5)
	fmt.Println("Less than: 10 < 5 =", 10 < 5)
	fmt.Println("Greater than or equal to: 10 >= 5 =", 10 >= 5)
	fmt.Println("Less than or equal to: 10 <= 5 =", 10 <= 5)
}