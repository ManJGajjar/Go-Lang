package main

import "fmt"

func ArithmeticOperators() {
	// Example of arithmetic operators
	fmt.Println("List of Arithmetic Operators in GoLang")
	fmt.Println("Addition: 5 + 3 =", 5+3)
	fmt.Println("Subtraction: 5 - 3 =", 5-3)
	fmt.Println("Multiplication: 5 * 3 =", 5*3)
	fmt.Println("Division: 6 / 3 =", 6/3)
	fmt.Println("Modulus: 5 % 3 =", 5%3)
}